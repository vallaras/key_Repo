x = (1, 2, 3, 4, 5, 6, 7, 8, 9)

# Slicing the Tuple using two indexes
a = x[2:6]
print("Both Indexes = ", a)
b = x[:6]
print("No First Index = ", b)
c = x[2:]
print("No Second Index = ", c)
d = x[:]
print("No Indexes = ", d)
e = x[-3:]
print("Negative First Index = ", e)
f = x[:-2]
print("Negative Second Index = ", f)
